<script>
import { BACKEND_URL } from '@/env.js'
import updateModal from '@/views/modals/books/Update.vue'
import deleteModal from '@/views/modals/books/Delete.vue'
export default {
    data() {
        return {
            products: [],
            pageController: {
                total: this.productTotal,
                chunks: this.chunks,
                currentPage: 1,
                totalPages: 0,
                focusTable: []
            },
            searchKeyword: '',
            title:'',
            genre:'',
            quantity:'',
            author:'',
            year:'',
            bookGenres: [
                "Fiction",
                "Non-fiction",
                "Mystery",
                "Thriller",
                "Romance",
                "Science Fiction",
                "Fantasy",
                "Horror",
                "Historical Fiction",
                "Young Adult",
                "Children",
                "Poetry",
                "Biography",
                "Self-help",
                "Business"
            ]
            
        }
    },
    components: {
        updateModal,
        deleteModal
    },
    methods: {
        chunkArray(array, chunkSize) {
            const chunks = [];
            for (let i = 0; i < array.length; i += chunkSize) {
                chunks.push(array.slice(i, i + chunkSize));
            }
            return chunks;
        },
        nextPage() {
            if (this.pageController.currentPage < this.pageController.totalPages) {
                this.pageController.currentPage += 1
                this.pageController.focusTable = this.currentChunk
            }
        },
        previousPage() {
            if (this.pageController.currentPage != 1) {
                this.pageController.currentPage -= 1
                this.pageController.focusTable = this.currentChunk
            }
        },
        getBooksData() {
            this.$http.get(`${BACKEND_URL}/api/v1/books`)
                .then((response) => {
                    this.products = response.data.books
                })
                .catch((error) => {
                    console.log(error);
                })
        },
        updateBookModal(id) {
            var cxx = document.getElementById(`updateBook-${id}`);
            console.log(cxx)
            cxx.showModal();
        },
        deleteBookModal(id) {
            document.getElementById(`deleteBook-${id}`).showModal();
        },
        created_book(){
            // Ajax Post Call to Backend
            this.$http.post(`${BACKEND_URL}/api/v1/books`, {
                title: this.title,
                author: this.author,
                genre: this.genre,
                quantity: this.quantity,
                year: this.year
            }).then((response) => {
                console.log(response)
                this.getBooksData()
                this.title = ''
                this.genre = ''
                this.quantity = ''
                this.author = ''
                this.year = ''
                window.location.reload()
            }).catch((error) => {
                console.log(error)
            })
        }
    },
    computed: {
        chunks() {
            return this.chunkArray(this.products, 10);
        },
        currentChunk() {
            return this.chunks[this.pageController.currentPage - 1]
        },
        currentRange() {
            if (this.pageController.total < this.pageController.currentPage * 10)
                return this.pageController.total
            else {
                return this.pageController.currentPage * 10
            }
        },
        productTotal() {
            return this.products.length
        },
    },
    mounted() {

    },
    created() {
        this.getBooksData()
    },
    updated() {

        if (this.searchKeyword == '') {
            var cr = this.chunkArray(this.products, 10);
            this.pageController.totalPages = cr.length
            this.pageController.focusTable = this.currentChunk
        }
    },
    watch: {
        searchKeyword: function (val) {
            if (this.searchKeyword != '' && this.searchKeyword.length > 3) {
                var query = this.searchKeyword.toLowerCase();
                this.pageController.focusTable = this.products.filter(book =>
                    book._id.toLowerCase().includes(query) ||
                    book.author.toLowerCase().includes(query) ||
                    book.genre.toLowerCase().includes(query) ||
                    book.title.toLowerCase().includes(query) ||
                    book.year.toString().includes(query)
                )
            }
        }
    }

}
</script>
